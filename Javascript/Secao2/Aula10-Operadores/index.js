// Operadores aritmeticos: + - * /
// + -> adicao e concatenacao
// ** -> potenciacao ex: 2 elevado ao quadrado
// % -> resto da divisao
// ++ -> incremento
// -- -> decremento
// NaN -> not a number (a conta deu algum erro)
// parseInt -> converte string para um numero inteiro ex: parseInt(valor a ser modificado)
// parseFloat -> converte string pra um numero decimal ex: parseFloat(valor a ser modificado)
// Number -> converte string para numero ex: Number(valor a ser modificado)

const num1 = 5;
const num2 = 10;
console.log(num1 + num2);

const n1 = '5';
const n2 = 10;
console.log(n1 + n2); // vai concatenar a string e o number
console.log(n1 ** n2);
